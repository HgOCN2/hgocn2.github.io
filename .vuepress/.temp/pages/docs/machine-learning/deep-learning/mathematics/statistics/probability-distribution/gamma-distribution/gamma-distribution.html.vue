<p>伽马分布用于预测未来事件发生前的等待时间。当某物的自然最小值为0时，它很有用。</p>
<p>伽玛分布一般和指数分布一起理解：</p>
<p>*1、从意义来看：*指数分布解决的问题是“要等到一个随机事件发生，需要经历多久时间”，伽玛分布解决的问题是“要等到n个随机事件都发生，需要经历多久时间”。</p>
<p>所以，伽玛分布可以看作是n个指数分布的独立随机变量的加总，即，n个Exponential(λ)random variables---&gt;Gamma(n,λ）</p>
