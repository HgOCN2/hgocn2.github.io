<p>能解答人类自由自发合作秩序、选举、贸易的起源之谜，P社玩家可以扮演议员跟GPT辩论竞选总统了。</p>
<p>在人类发明语言以前，有复杂社会合作的生物基本上不能维持每个个体独立繁殖。蚂蚁、蜜蜂的繁殖方式实际上把整个巢穴变成了个体、把每个个体变成了细胞。</p>
<p>人以外几乎唯一能组成社会还独立繁殖的例子是虎鲸，社会化程度低一些，而且已经有初步的语言了。社会形态相近但没有多少复杂度的低配版：狼，也和早期智人、虎鲸一样组成母系社会在开阔地带团战，狗狗也有很丰富的情感。大猩猩和黑猩猩的社会合作程度都不如狼群。</p>
<p>演化博弈论模型《自私的基因》和《强互惠的演化》试图解释人类合作，模拟出的都是昆虫左逼社会，而且规模仅限于120人以下。（这个数字接近于邓巴数，一个人能认识人的数量，反过来说邓巴数可能由类似的过程确定。）</p>
<p>既要复杂合作又要个体独立性，必须要个体出现语言能力。经济学里面画曲线的分析方式不够用，经典的博弈论也不够用。</p>
<p>实际上斯坦福哲学百科“演化博弈论”最后一条就是语言起源，而OpenAI在2017年的一个小游戏里实现了语言起源，相当于现在这个模型的低配版。</p>
