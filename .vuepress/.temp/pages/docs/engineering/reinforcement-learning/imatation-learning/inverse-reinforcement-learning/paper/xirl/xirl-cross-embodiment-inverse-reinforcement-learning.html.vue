<p>paper: <a href="https://arxiv.org/pdf/2106.03911.pdf" target="_blank" rel="noopener noreferrer">XIRL: Cross-embodiment Inverse Reinforcement Learning<ExternalLinkIcon/></a></p>
<p>虽然机器人的数据很少，但是人类执行不同任务的视频却很多，当然机器人和人的构造有不同之处，因此让机器人向人类学习的想法引发了「 跨不同实体迁移学习」的问题。</p>
<p>研究人员开发了交叉具身反向强化学习（Cross-Embodiment Inverse Reinforcement Learning），通过观察人类来学习新的任务，并非是试图像人类那样精确地复制任务，而是学习高层次的任务目标，并以奖励函数的形式总结这些知识，示范学习可以让机器人通过观看互联网上随时可用的视频来学习技能。</p>
